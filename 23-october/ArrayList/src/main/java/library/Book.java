package arraylist;

/** Creates a Book object.
 *
 * @author Douglas Luman
 */
public class Book {

  public final String title;
  public final String author;

  /** Constructor.
   *
   * @param title Book title
   * @param author Book author
   */
  public Book (String title, String author) {
    this.title = title;
    this.author = author;
  }
}
